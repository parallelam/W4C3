// Some object methods

var iceCreamFlavorColors = {
  chocolate: 'brown',
  vanilla: 'white',
  strawberry: 'red'
};

console.log(Object.keys(iceCreamFlavorColors)); // ['chocolate', 'vanilla', 'strawberry']

console.log(Object.values(iceCreamFlavorColors)); // ['brown', 'white', 'red']

console.log(Object.entries(iceCreamFlavorColors)); // [ [ 'chocolate', 'brown' ], [ 'vanilla', 'white' ], [ 'strawberry', 'red' ] ]
